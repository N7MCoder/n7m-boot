import cc.nunumao.passport.config.security.extension.official.ClientSecretOfficialAuthenticationConverter;
import cc.nunumao.passport.config.security.extension.official.ClientSecretOfficialAuthenticationProvider;
import cc.nunumao.passport.config.security.extension.password.OAuth2PasswordAuthenticationConverter;
import cc.nunumao.passport.config.security.extension.password.OAuth2PasswordAuthenticationProvider;
import cc.nunumao.passport.config.security.handle.OAuth2AuthenticationFailureHandler;
import cc.nunumao.passport.config.security.handle.OAuth2AuthenticationSuccessHandler;
